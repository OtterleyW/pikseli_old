				<h2>Tallialue</h2>

				<p>Hukkapuron tallialuekompleksi on kasvanut kuin pieni hökkelikylä vähän kerrallaan. Tällä hetkellä alue koostuu kahdesta vastarakennetusta, mutta melko yksinkertaisesta päätallista, pienemmästä navetasta remontoidusta kivitallista sekä kahdesta pihatosta tallialueen laidalla. Tämän lisäksi tallialueelta löytyy kaksi kenttää sekä hökkeli, joka näyttelee maneesin roolia, mutta on kuitenkin tarpeeksi epämääräinen rakennelma, jotta rakennusvirasto ei kiinnostuisi sen puutteellisista rakennusluvista turhan paljoa. Lisäksi tallin pihapiiristä löytyy aidatut tarhat ja Wilsonien perheen kotitalo. Kävelytyskoneita, infrapunasaunoja tai muita luksustallien hömpötyksiä on turha Hukkapurosta etsiä, mutta sentään tallirakennnukset on lämmitetty ja niistä löytyy juokseva vesi sekä sähköt. Uusin yleellisyys on karsinoihin asennetut juoma-automaatit, sillä kuka muka jaksaisi kantaa ämpäreissä vettä satapäiselle hevoslaumalle.</p>

				<p>Tallin kentät ovat hyväpohjaiset ja aidatut ja vihdoin syksyllä 2015 saatiin kenttien laidalle myös valot niin että ratsastus onnistuu iltojen pimetessäkin. Talvella kenttä saattaakin olla järkevämpi ratkaisu, sillä edeleen maneesin lämmityksestä on turha edes haaveilla ja talvisin onkin hyvin mahdollista, että mokoman peltipurkin sisällä on jopa kylmempää kuin ulkona. Hukkapuron maastoratsastusmahdollisuudet sitä vastoin ovat erinomaiset ja erilaisia maastopolkuja sekä osittain sammalta kasvavia maastoesteitä lötytyy läheisestä Hukkametsästä kilometritolkulla - harmi vain, ettei maastoköpöttely kiinnosta juurikaan ketään meillä Hukkapurossa. Onkin kai turha sanoa, että Otterley haaveilee tallialueen täydellisestä uudistamisesta niin, että tilukset todella vastaisivat GP-tason ratsastajan tarpeita.</p>
			
